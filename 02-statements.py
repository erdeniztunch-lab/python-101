# Python Statements

# Statements

# A computer program is a list of "instructions" to be "executed" by a computer.

# In a programming language, these programming instructions are called statements.

# ---------

# Example

print("Python is fun!")

# This statement tells Python to print text on the screen

# ---------

# Important idea:
# Python programs usually run line by line. Each line is executed in the order it appears.

# ---------

# Example

print("Hello World!")
print("Have a good day.")
print("Learning Python is fun!")

# Python runs the first line, then the second and then the third.

# About semicolons:
# In python you usually do not use ";". Each statement is written on a new line to keep the code easy to read.
