# Python print() and Output

# Print text
# print() is used to show text or values on the screen.

# Example
print("Hello World!")

# This prints Hello World! to the screen.

# You can use print() many times. Each call prints on a new line by default.

# -----------

# Example

print("Hello world!")
print("Iam learning Python.")
print("It is awesome!")

# Python prints these lines one under another.

# -----------

# Quotes
# Text in Python must be inside quotes.

# You can use double quotes ("") or single (') quotes.

# Example

print("This will work!")
print('This will also work!')

# Both are valid.

# If you forget the quotes, Python gives an error because it thinks the text is code.

# Example

# print(This will cause an error)

# Result: Syntax Error: invalid syntax

# -----------

# Print without a new line

# Normally print() moves to a new line after printing.

# If you want to continue on the same line, use "end" parameter.

# Example

print("Hello World!", end=" ")
print("I will print on the same line.")

# Result: Hello World! I will print on the same line.
