# Good to know

# New lines for commands
# In Python, each line is usually one command. You don't need semicolons (;) at the end like in some other languages.

# Indentation defines scope
# Python uses spaces or tabs at the start of a line to show which code belongs together, like inside a loop, function, or class. Other languages often use curly braces ({}) for this.
