# Concatenation
# Concatenation means joining strings together.

# In Python, you can combine strings using the + operator.

# -----------------------------

# Combine two strings
# Example

a = "Hello"
b = "World"
c = a + b
print(c)

# Output: Helloworld

# Python simply joins the strings together.

# -----------------------------

# Add a space between words
# If you want a space, you must add it manually.

# Example

a = "Hello"
b = "World"
c = a + " " + b
print(c)

# Output: Hello World

# -----------------------------

# Another example

first_name = "John"
last_name = "Smith"
full_name = first_name + " " + last_name

print(full_name)
