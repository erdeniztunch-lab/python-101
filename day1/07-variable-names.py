# Python Variable Names

# What are variable names?
# A variable name is the label you give to a value. It can be short (x) or descriptive (age, car_name).

# Rules for Python variable names
'''
1. Must start with a letter or underscore_
2. Cannot start with a number
3. Can only contain letters, numbers, and underscores (A-z, 0-9, _)
4. Case-sensitive (age, Age, AGE are different)
5. Cannot be a Python keyword (if, for, class, etc.)
'''

# Examples

myvar = "John"
my_var = "John"
_my_var = "John"
myVar = "John"
MYVAR = "John"
myvar2 = "John"

# Wrong examples
'''
2myvar = "John" - starts with number
my-var = "John" - contains dash
my var = "John" - contains space

'''

# Multi-word variable names
# To make them readable, use:

# Camel Case: first word lowercase, others capitalized
myVariableName = "John"

# Pascal Case: all words capitalized
MyVariableName = "John"

# Snake Case: word separated by underscore
my_variable_name = "John"
