# Python - Output Variables

# Print a single variable

# Use print() to show the value stored in a variable.

# Example

x = "Python is awesome"
print(x)

# Output: "Python is awesome"

# Print multiple variables
# You can print several variables separated by a comma. Python adds a space automatically.

y = "Python"
z = "is"
u = "awesome"
print(y, z, u)

# Output: "Python is awesome"

# Combine variables with +
# You can also use + to join strings, but you need spaces manually.

q = "Python "
w = "is "
e = "awesome"
print(q + w + e)

# For numbers, + adds them mathematically:
o = 5
l = 10
print(o + l)

# Output: 15

# Mixing strings and numbers with +
# You can print different data types together by separating them with commas:

f = 5
r = "John"
print(f, r)

# Output: 5 John
